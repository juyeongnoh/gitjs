const { hashObject } = require("../plumbings");
const Entry = require("../types/Entry");
const Index = require("../types/Index");

describe("Index", () => {
  const indexSample = Buffer.concat([
    Buffer.from([0x44, 0x49, 0x52, 0x43]), // Signature "DIRC"
    Buffer.from([0x00, 0x00, 0x00, 0x02]), // Version 2
    Buffer.from([0x00, 0x00, 0x00, 0x01]), // Entry count (1 entry)
    Buffer.from([0x68, 0xae, 0x89, 0x91, 0x31, 0x1c, 0x40, 0x53]), // ctime
    Buffer.from([0x68, 0xae, 0x89, 0x91, 0x31, 0x1c, 0x40, 0x53]), // mtime
    Buffer.from([0x01, 0x00, 0x00, 0x0f]), // dev
    Buffer.from([0x00, 0x3f, 0x1f, 0xad]), // ino
    Buffer.from([0x00, 0x00, 0x81, 0xa4]), // mode
    Buffer.from([0x00, 0x00, 0x01, 0xf5]), // uid
    Buffer.from([0x00, 0x00, 0x00, 0x14]), // gid
    Buffer.from([0x00, 0x00, 0x00, 0x10]), // fileSize
    Buffer.from([
      0x94, 0x74, 0x3c, 0xdf, 0xef, 0xd3, 0x3a, 0xb9, 0x8a, 0xbf, 0x8f, 0x09,
      0xcf, 0x49, 0x14, 0x17, 0x39, 0xeb, 0x8e, 0x52,
    ]), // objectName
    Buffer.from([0x00, 0x08]), // flags
    Buffer.from([0x74, 0x65, 0x73, 0x74, 0x66, 0x69, 0x6c, 0x65, 0x00, 0x00]), // entryPathname
    Buffer.from([
      0x7f, 0x9d, 0x0a, 0x6a, 0xc0, 0x29, 0x38, 0xab, 0x5e, 0x9b, 0xce, 0x48,
      0x0d, 0x33, 0x8d, 0x65, 0xf9, 0x00, 0x35, 0x73,
    ]), // checksum
  ]);

  test("can parse index sample", () => {
    // content: can't touch this
    const testfile = Buffer.from([
      0x63, 0x61, 0x6e, 0x27, 0x74, 0x20, 0x74, 0x6f, 0x75, 0x63, 0x68, 0x20,
      0x74, 0x68, 0x69, 0x73,
    ]);

    const mockStats = {
      dev: 16777231n,
      mode: 33188n,
      uid: 501n,
      gid: 20n,
      ino: 4136877n,
      size: 16n,
      mtimeMs: 1756268945823n,
      ctimeMs: 1756268945823n,
      mtimeNs: 1756268945823935059n,
      ctimeNs: 1756268945823935059n,
      mtime: new Date("2025-08-27T04:29:05.823Z"),
      ctime: new Date("2025-08-27T04:29:05.823Z"),
      isSymbolicLink: () => false,
    };

    const entry = Entry.fromFileStats(
      mockStats,
      hashObject(testfile, "blob", null),
      "testfile".length,
      "testfile"
    );

    const index = new Index();
    index.addEntry(entry);

    expect(Buffer.compare(indexSample, index.toBuffer())).toBe(0);
  });
});
